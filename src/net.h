    void AddInventoryKnown(const CInv& inv)
    {
		loop()
		{
		    {
		        TRY_LOCK(cs_inventory, lockInv);
				if (lockInv)
				{
		        	setInventoryKnown.insert(inv);
					break;
				}
				else
				{
					Sleep(20);
					continue;
				}
		    }
		}
    }

